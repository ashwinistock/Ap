package ashwini;

public class ifelse {

	public static void main(String[] args) { // ladder effect marksheet
		
		int marks = 86;
		if ( 90 <= marks ) {
			System.out.println("grade A");
		}
		else 
			if( 80 <= marks) {
				System.out.println("grade B");
			}
			else 
				if( 98 <= marks) {
					System.out.println("grade C");
				}
	}
	
}
